  content += "//\n";
  content += "// This file is yours to edit. 'rexglue migrate' will NOT overwrite it.\n";
  content += "// Customize your app by overriding virtual hooks from rex::ReXApp.\n";
  content += "\n";
  content += "#pragma once\n";
  content += "\n";
  content += "#include <rex/rex_app.h>\n";
  content += "\n";
  content += "class " + class_name + " : public rex::ReXApp {\n";
  content += " public:\n";
  content += "  using rex::ReXApp::ReXApp;\n";
  content += "\n";
  content += "  static std::unique_ptr<rex::ui::WindowedApp> Create(\n";
  content += "      rex::ui::WindowedAppContext& ctx) {\n";
  content += "    return std::unique_ptr<" + class_name + ">(new " + class_name + "(ctx, \"" +
             names.snake_case + "\",\n";
  content += "        PPCImageConfig));\n";
  content += "  }\n";
